<script>
/**
 * The Vue Module that displays information of one single user
 *
 * @author He, Hao
 * @since  2019-01-02
 */

const echarts = require("echarts");
const userdata = require("../dist/users.json");
const maildata = require("../dist/mails.json");
const eventBus = require("../src/EventBus.js");
const keywordExtraction = require("../src/Keyword.js");

module.exports = {
    data: () => ({
        // user ID
        id: -1,
        // Filters
        beginDate: null,
        endDate: null
    }),
    components: {
        UserActivityPlot: require("./ActivityPlot.vue"),
        UserKeywordCloud: require("./WordCloud.vue"),
        UserMailList: require("./MailList.vue"),
        UserRelated: require("./SortedBarChart.vue")
    },
    computed: {
        name() {
            return userdata[this.id].name;
        },
        address() {
            return userdata[this.id].address;
        },
        mailIds() {
            return userdata[this.id].mails;
        },
        activity() {
            let result = [];
            let resultIdMap = new Map();

            let minDate = new Date(maildata[this.mailIds[0]].date);
            let maxDate = new Date(maildata[this.mailIds[0]].date);
            for (let i = 0; i < this.mailIds.length; ++i) {
                let date = new Date(maildata[this.mailIds[i]].date);
                if (date < minDate) minDate = date;
                if (date > maxDate) maxDate = date;
            }
            for (
                let i = minDate.getFullYear();
                i <= maxDate.getFullYear();
                ++i
            ) {
                for (let j = 1; j <= 12; ++j) {
                    let ym = i + "-" + j;
                    result.push([ym, 0]);
                    resultIdMap.set(ym, result.length - 1);
                }
            }

            for (let i = 0; i < this.mailIds.length; ++i) {
                let date = new Date(maildata[this.mailIds[i]].date);
                let ym = date.getFullYear() + "-" + date.getMonth();

                let activityIndex = resultIdMap.get(ym);
                if (activityIndex != undefined) {
                    result[activityIndex][1]++;
                }
            }
            return result;
        },
        keywords() {
            let m = [];
            for (let i = 0; i < this.mailIds.length; ++i) {
                let flag = true;
                let date = new Date(maildata[this.mailIds[i]].date);
                if (this.beginDate) {
                    flag &= date > this.beginDate;
                }
                if (this.endDate) {
                    flag &= date < this.endDate;
                }
                if (flag) {
                    m.push(maildata[i]);
                }
            }
            return keywordExtraction
                .generateKeywords(m)
                .filter((word, index) => index <= 50);
        },
        relatedUsers() {
            let result = [];
            let resultIdMap = new Map();
            for (let i = 0; i < this.mailIds.length; ++i) {
                let currMailId = this.mailIds[i];
                let mail = maildata[currMailId];
                let currUserIds = [];
                if (mail.inReplyTo != undefined)
                    currUserIds.push(maildata[mail.inReplyTo].userId);
                if (mail.replies != undefined)
                    mail.replies.forEach(r => {
                        currUserIds.push(maildata[r].userId);
                    });
                if (mail.references != undefined)
                    mail.references.forEach(r => {
                        currUserIds.push(maildata[r].userId);
                    });
                if (mail.citations != undefined)
                    mail.citations.forEach(c => {
                        currUserIds.push(maildata[c].userId);
                    });

                currUserIds.forEach(currUserId => {
                    if (currUserId === -1 || currUserId === this.id) return;
                    let resultId = resultIdMap.get(currUserId);
                    if (resultId === undefined) {
                        result.push({
                            id: currUserId,
                            name: userdata[currUserId].name,
                            value: 1
                        });
                        resultIdMap.set(currUserId, result.length - 1);
                    } else {
                        result[resultId].value++;
                    }
                });
            }
            result.sort((a, b) => {
                if (a.value > b.value) return -1;
                else if (a.value < b.value) return 1;
                return 0;
            });
            return result.filter((user, index) => index <= 15);
        }
    },
    created: function() {
        // For test of this module
        let userId = 0;
        for (let i = 0; i < userdata.length; ++i) {
            if (userdata[i].name === "Michael Jackson") {
                userId = i;
                break;
            }
        }
        // Basic data
        this.id = userdata[userId].id;
    },
    mounted: function() {
        eventBus.$on("date-filter-changed", param => {
            // This event should not be responded
            if (!param.tag.includes("UserOverview")) {
                return;
            }
            // Set the new date filter
            this.beginDate = param.beginDate;
            this.endDate = param.endDate;
        });
        eventBus.$on("user-changed", param => {
            this.beginDate = null;
            this.endDate = null;
            this.id = param.userId;
        });
    },
    methods: {}
};
</script>

<template>
    <div id="User">
        <div id="UserOverview">
            <h3>{{name}}</h3>
            <user-activity-plot
                v-bind:data="this.activity"
                tag="UserOverview"
                style="width:100%; height:200px;"
            ></user-activity-plot>
        </div>
        <div id="WordCloud">
            <user-keyword-cloud
                v-bind:data="this.keywords"
                tag="keyword"
                style="width:100%; height:200px;"
            ></user-keyword-cloud>
        </div>
        <div id="MailList">
            <user-mail-list
                :mailIds="this.mailIds"
                :beginDate="this.beginDate"
                :endDate="this.endDate"
            ></user-mail-list>
        </div>
        <div id="SortedBarChart">
            <user-related :data="this.relatedUsers" style="width:100%; height:200px;"/>
        </div>
    </div>
</template>

